/*
 *      dev - MMD
 *      addr - register address
 * Output:
 *      data - register data
 * Return:
 *      SUCCESS
 *      FAILURE
 * Note:
 *      None
 */
int32 MmdPhyRead(struct phy_device *phydev,UINT16 dev,UINT16 addr,UINT16 *data)
{
    int val;

    val = phy_read_mmd(phydev, dev, addr);
    if (val < 0)
        return FAILURE;     
    *data = (UINT16)val;

    return SUCCESS;
}
/*
 *      dev - MMD
 *      addr - register address
 *      data - register data
 * Return:
 *      SUCCESS
 *      FAILURE
 * Note:
 *      None
 */
int32 MmdPhyWrite(struct phy_device *phydev,UINT16 dev,UINT16 addr,UINT16 data)
{
    int ret = phy_write_mmd(phydev, dev, addr, data);
    return (ret < 0) ? FAILURE: SUCCESS;
}


static BOOL Rtl8251b_wait_for_bit(struct phy_device *phydev,UINT16 dev,UINT16 addr,UINT16 mask,BOOL set,UINT16 timeoutms)
{
    BOOL status = FAILURE;
    UINT16 phydata = 0;

    while (--timeoutms) {
        status = MmdPhyRead(phydev, MMD_VEND2, addr, &phydata);
        if (!status)
            goto err_exit;

        if (!set)
            phydata = ~phydata;

        if ((phydata & mask) == mask)
            return SUCCESS;

        msleep(1);
    }

    osal_printf("Timeout (dev=%02x addr=0x%02x mask=0x%02x timeout=%d)\n",
          dev, addr, mask, timeoutms);

err_exit:
    return FAILURE;
}

